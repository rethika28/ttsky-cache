`timescale 1ns / 1ps

module tb;

    //--------------------------------------------------
    // Signals
    //--------------------------------------------------

    reg  [7:0] ui_in;
    wire [7:0] uo_out;

    reg  [7:0] uio_in;
    wire [7:0] uio_out;
    wire [7:0] uio_oe;

    reg ena;
    reg clk;
    reg rst_n;

    //--------------------------------------------------
    // DUT Instantiation
    //--------------------------------------------------

    tt_um_example DUT (
        .ui_in(ui_in),
        .uo_out(uo_out),

        .uio_in(uio_in),
        .uio_out(uio_out),
        .uio_oe(uio_oe),

        .ena(ena),
        .clk(clk),
        .rst_n(rst_n)
    );

    //--------------------------------------------------
    // Clock Generation
    //--------------------------------------------------

    always #5 clk = ~clk;

    //--------------------------------------------------
    // Test Procedure
    //--------------------------------------------------

    initial
    begin

        //----------------------------------------------
        // Initialize
        //----------------------------------------------

        clk   = 0;
        ena   = 1;
        rst_n = 0;

        ui_in  = 8'b00000000;
        uio_in = 8'b00000000;

        //----------------------------------------------
        // Reset
        //----------------------------------------------

        #20;
        rst_n = 1;

        //----------------------------------------------
        // WRITE Operation
        //----------------------------------------------
        // Address = 2
        // Data    = 25
        //----------------------------------------------

        #10;

        ui_in[1:0] = 2'b10;      // address
        ui_in[7:2] = 6'd25;      // write data

        uio_in[0] = 1'b1;        // write enable

        #10;

        //----------------------------------------------
        // Disable Write
        //----------------------------------------------

        uio_in[0] = 1'b0;

        //----------------------------------------------
        // READ Operation
        //----------------------------------------------

        #10;

        ui_in[1:0] = 2'b10;

        #10;

        //----------------------------------------------
        // Display Output
        //----------------------------------------------

        $display("Cache Read Data = %d", uo_out);

        //----------------------------------------------
        // Finish
        //----------------------------------------------

        #20;
        $finish;

    end

    //--------------------------------------------------
    // Waveform Dump
    //--------------------------------------------------

    initial
    begin
        $dumpfile("cache.vcd");
        $dumpvars(0, tb);
    end

endmodule
